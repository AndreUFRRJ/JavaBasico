
public class Main {
	public static void main(String args[]) {
		/*
		 * comparadores de valor existentes
		 * < menor
		 * > maior
		 * >= maior ou igual
		 * <= menor ou igual
		 * == igual
		 * != diferente 
		 */
		
		int x=9;
		int y=10;
		
		System.out.println(x<y);
		System.out.println(x>y);
		System.out.println(x>=y);
		System.out.println(x<=y);
		System.out.println(x==y);
		System.out.println(x!=y);
	}
}
